let diaDaSemana = 'domingo'
switch (diaDaSemana){
    case 'sexta':
        console.log('sextou!')
        break
    case 'sábado':
        console.log('sabadou')
        break
    case 'domingo':
        console.log('fim de semana tá acabando :(')
        break
    default:
        console.log(`${diaDaSemana} é dia de trabalho.`)
}